    // TODO: Rename and change types of parameters
    public static MessageFragment newInstance(final int conversationId) {
        MessageFragment fragment = new MessageFragment();
        Bundle args = new Bundle();
        args.putInt(ARG_CONVERSATION, conversationId);
        fragment.setArguments(args);
        return fragment;
    }

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);

        if (getArguments() != null) {
            conversationId = getArguments().getInt(ARG_CONVERSATION);
        }

        ConversationRepository repository = ConversationRepository.getRepository();

        Conversation conversation = repository.getConversationById(conversationId);

        mAdapter = new ArrayAdapter<Message>(getActivity(),
                android.R.layout.simple_list_item_1, android.R.id.text1, conversation.messages);
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        View view = inflater.inflate(R.layout.fragment_message, container, false);

        // Set the adapter
        mListView = (AbsListView) view.findViewById(android.R.id.list);
        mListView.setAdapter(mAdapter);

        // Set OnItemClickListener so we can be notified on item clicks
        mListView.setOnItemClickListener(this);

        return view;
    }

